/**
 * Chat Types for Genesis App
 */

export interface User {
  id: string;
  name: string;
  email: string;
  image?: string | null;
  status?: 'online' | 'offline' | 'away' | 'busy';
}

export interface Reaction {
  emoji: string;
  count: number;
  users: User[];
  hasReacted: boolean;
}

export interface Message {
  id: string;
  content: string;
  authorId: string;
  author: User;
  channelId: string;
  parentId?: string | null;
  createdAt: Date;
  updatedAt: Date;
  editedAt?: Date | null;
  reactions: Reaction[];
  replyCount: number;
  replyPreview?: Message[];
  mentions: User[];
  attachments: Attachment[];
  isDeleted?: boolean;
}

export interface Attachment {
  id: string;
  name: string;
  url: string;
  type: 'image' | 'file' | 'video' | 'audio';
  size: number;
  mimeType: string;
}

export interface Channel {
  id: string;
  name: string;
  description?: string;
  type: 'public' | 'private' | 'direct';
  workspaceId: string;
  createdAt: Date;
  members: User[];
  unreadCount: number;
  lastMessage?: Message;
}

export interface Thread {
  parentMessage: Message;
  messages: Message[];
  participants: User[];
}

export interface TypingUser {
  user: User;
  channelId: string;
  timestamp: number;
}

export interface SendMessageInput {
  content: string;
  channelId: string;
  type?: 'TEXT' | 'SYSTEM' | 'FILE' | 'COMMAND';
  parentId?: string;
  metadata?: Record<string, unknown>;
  mentions?: string[];
  attachments?: File[];
}

export interface UpdateMessageInput {
  content: string;
  metadata?: Record<string, unknown>;
}

export interface MessageFilters {
  before?: string;
  after?: string;
  limit?: number;
  search?: string;
}

export interface EmojiCategory {
  id: string;
  name: string;
  emojis: string[];
}

